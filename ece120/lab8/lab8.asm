;introduction:
;this program will render a null-terminated string in to a large front to the monitor


;approach:
;every ASCII character contain 16 rows and 8 columns.
;we need to use the null-terminated string stored from x5002 to find the starting address in the FONT_DATA
;print the character line by line.
;after finishing one row for one character, we deal with the same row for another character
;until we find null character and then move we next row
;after checking all 16 rows, we halt the program.


;register table
;R0: store the ASCII character and temporary register
;R1: temporary register
;R2: row counter, has an initial value 16
;R3: column counter, has an initial value 8
;R4: temporary register
;R5: address register, contain the address in the FONT_DATA
;R6: temporary register


;initialization
	.ORIG x3000			;program starts from memory location x3000
	LD R4, FIVEK			;load the value x5000 to R4
	LEA R5, FONT_DATA		;load the address of FONT_DATA to R5
	LD  R1, FIVETWO			;load the value x5002 to R1


;set row counter to be 16
	LD R2, SIXTEEN			;load the value #16 to R2

;check the row condition. If R2 is zero, we halt we program
NEXT_ROW
	ADD R2, R2, #0			;increment R2 by o
	BRz  DONE			;Branch to DONE if R2 is zero
	LDR R0, R4, #2			;load the value at x5002 to R0


;check the condition of column. if R3 is zero, we move to next row
NEXT_CHAR
	LD  R3, EIGHT			;load the value #8 to R3
DONE_COLUMN
	ADD R3, R3, #0			;increment R3
	BRz  NEW_LINE			;Branch to NEW_LINE if R3 is zero


;find the address in FONT_DATA
	ADD R0, R0, #0			;increment R0 by 0
	BRz NEW_LINE			;Branch to NEW_LINE if R0 is zero. Beware the nullstring
	AND R4, R5, #-1			;store the value of R5 in R4
	AND R6, R0, #-1			;store the value of R1 in R6
LOOP	ADD R4, R4, #15			;increment R4 by 15
	ADD R4, R4, #1			;increment R4 by 1
	ADD R6, R6, #-1			;decrement R6 by -1
	BRp LOOP			;Branch to LOOP if R6 is positive


;deal with the each bit
	LDR R6, R4, #0			;load the value of the address stored in R4 to R6
NEXT_COLUMN
	LD  R4, FIVEK			;load the value x5000 to R4
	AND R6, R6, #-1			;store the value of R6 in R6
	BRzp ZERO			;Branch to ZERO if R6 is zero or positive
	LDR R0, R4, #1			;load the value at x5001 to R0
	BRnzp  OT			;Branch to OT no matter what value R0 is
ZERO	LDR R0, R4, #0			;load the value at x5000 to R0
OT	OUT				;print to screen the ASCII value stored in R0
	ADD R6, R6, R6			;left shift R6
	ADD R3, R3, #-1			;decrement R3 by 1
	BRz DONE_EIGHT			;Branch to DONE_EIGHT if R3 is R3 is zero
	BRnzp NEXT_COLUMN		;Branch to NEXT_COLUMN no matter what value R3 is

;check if the next character is null character
DONE_EIGHT
	ADD R1, R1, #1			;increment R1 by 1
	LDR R0, R1, #0			;load the value of address stored in R1 to R0
	AND R6, R0, R0			;store the value of R0 to R6
	BRnp NEXT_CHAR			;Branch to NEXT_CHAR if R6 is negative or positive
	BRz  DONE_COLUMN		;Branch to DONE_COLUMN if R6 is zero


;after we finish all column
NEW_LINE
	LD  R0, NL			;load the value xA to R0
	OUT				;print to screen the ASCII value stored in R0. Move to next line
	ADD R5, R5, #1			;increment R5 by 1
	ADD R2, R2, #-1			;decrement R2 by 1
	LD  R1, FIVETWO			;load the value x5002 to R1
	BRnzp  NEXT_ROW			;Branch to NEXT_ROW no matter what value R1 is
	
DONE	HALT				;TRAP x25, halt

FIVEK	.FILL	x5000			;places value x5000 at that code line.
EIGHT	.FILL	x0008			;places value x0008 at that code line. The value is #8
SIXTEEN	.FILL	x0010			;places value x0010 at that code line. the value is #16
FIVETWO	.FILL	x5002			;places value x5002 at that code line.
NL	.FILL	xA			;places value xA at that code line.


