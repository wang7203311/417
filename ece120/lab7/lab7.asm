;Introduction:
;this program renders a single ASCII character in a large font to the monitor.

;Approach:
;in the monitor, every ASCII character contain 16 rows and 8 columns
;we need use the ASCII character stored at x5002 to find the starting address in the FONT_DATA
;for one row, we check the bit one by one. Until we checked all 8 columns, we move to next row
;after checking all 16 rows, we halt the program.

;Register Table:
;R0 ASCII register
;R1 save character register
;R2 row counter
;R3 column counter
;R4 temporary register
;R5 address register, contain the address in the FONT_DATA
;R6 second temporary register 


;initialization
	.ORIG X3000
	LD R4, FIVEK
	LDR R1, R4, #2
	LEA R5,FONT_DATA


;set row counter to 16
	AND R2, R2,#0		;clear the row counter register
	ADD R2, R2,#15		;increment R2 by 15
	ADD R2, R2,#1 		;increment R2 by 1


;starting adress	
LOOP	ADD R5, R5,#15		;increment R5 by 15
	ADD R5, R5,#1		;increment R5 by 1
	ADD R1, R1,#-1		;decrement R1 by 1
	BRp LOOP		;branch to LOOP if R1 is positive


;R2 !=0?
NEXT_ROW
	ADD R2, R2,#0		;increment R2 by 0
	BRz DONE		;branch to DONE if R2 is zero

;R3 !=0?
	LD R3, EIGHT		;load the value x0008 to R3
DONE_COLUMN
	ADD R3, R3,#0		;increment R3 by 0
	BRz NEW_LINE		;branch to NEW_LINE if R3 is zero


;if R3 is not 0, check the bit is 0 or 1. After checking all bits, change to next row
	LDR R6, R5,#0		;load the value of address stored in R5 to R6
NEXT_COLUMN
	ADD R6, R6,#0		;AND R6 with #-1 and store in R6
	BRzp ZERO		;branch to ZERO if R6 is positive or zero
	LDR R0, R4,#1		;load value at X5001 to R0
	BRnzp OT		;branch to OT no matter what value R0 is
ZERO	LDR R0, R4,#0		;load value at X5000 to R0
OT	OUT			;print to screen the ASCII value stored at R0
	ADD R6, R6,R6		;left shift the value in R6
	ADD R3, R3,#-1  	;decreement R3 by 1
	BRz DONE_COLUMN  	;branch to NEXT_COLUMN if R4 is positive or negative
	ADD R3, R3,#0		;increment R3 by 0
	BRnzp NEXT_COLUMN	;branch to NEXT_COLUMN no matter what value R3 is

;if R3 is 0, we change to next row
NEW_LINE
	LD R0, NL		;load NewLine ASCII value
	OUT			;print to screen the ASCII value stored at R0
	ADD R5, R5,#1		;increment R5 by 1
	ADD R2, R2,#-1		;decrement R2 by 1
	BRnzp NEXT_ROW		;branch to NEXT_ROW no matter what value R2 is


;If R2 is 0, we processes all bits
DONE	HALT			;halt program
 

